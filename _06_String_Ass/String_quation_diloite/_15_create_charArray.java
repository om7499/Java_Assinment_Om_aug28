package String_quation_diloite;

public class _15_create_charArray {

	public static void main(String[] args) {
		String str = "java programing";
		char[] ch = str.toCharArray();
		for(int i=0;i<ch.length;i++) {
			System.out.print(ch[i]+" ");
		}

	}

}

